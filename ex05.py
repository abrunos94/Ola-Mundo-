# Exercicio de Aritimetica
n1 = int(input("Digite um numero:"))
n2 = int(input("Digite um numero:"))
s = n1 + n2   # Adiçao
m = n1 * n2   # Multiplicaçao
d = n1 / n2   # Divisao
di = n1 // n2 # Divisao inteira
e = n1 ** n2  # Elevado
print(" A soma é {}; \n Multiplicação é {}; \n Divisão {};".format(s, m, d))
print(" Divisão interira {}; \n Potencia {};" .format(di, e))
